# Arithmetic Operators

#addition
print("Addition:")
print(5 + 3)

#subtraction
print("Subtraction:")
print(10 - 4)

#multiplication
print("Multiplication:")
print(6 * 7)

#division
print("Division:")
print(8 / 2)

#integer division
print("Integer Division:")
print(8 // 2)

#modulus
print("Modulus:")
print(8 % 2)

#power/exponentiation
print("Power/Exponentiation:")
print(8 ** 2)


# Comparison Operators
a = 10
b = 20
print("Comparison Operators:")
print("a == b:", a == b)
print("a != b:", a != b)
print("a < b:", a < b)
print("a > b:", a > b)
print("a <= b:", a <= b)
print("a >= b:", a >= b)


# Logical Operators
x = True
y = False
print("Logical Operators:")
print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)
print("not y:", not y)