# Data Types

# Integer data type
age = 25
print("Age:", age)
print("Type of age:", type(age))
# Float data type
height = 5.9
print("Height:", height)
print("Type of height:", type(height))
# String data type
name = "Alice"
print("Name:", name)
print("Type of name:", type(name))
# Boolean data type
is_student = True
print("Is Student:", is_student)
print("Type of is_student:", type(is_student))
# None data type
address = None
print("Address:", address)
print("Type of address:", type(address))


# Type Conversion

# Convert to Integer
x = int(5.7)
print(x)   # 5
print("Type after conversion:", type(x))


# Convert to Float
y = float(10)
print(y)   # 10.0
print("Type after conversion:", type(y))

# Convert to String
age = 20
print("My age is " + str(age))
print("Type after conversion:", type(str(age)))

# Convert String to Number
num = int("10")
print(num + 5)   # 15
print("Type after conversion:", type(num))
