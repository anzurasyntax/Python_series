# Conditional Statements


# if statement
x = 10
if x > 5:
    print("x is greater than 5")

# if-else statement
y = 3
if y < 5:
    print("y is less than 5")
else:
    print("y is not less than 5")

# else statement
z = 2
if z == 5:
    print("z is equal to 5")
elif z > 5:
    print("z is greater than 5")
else:
    print("z is less than 5")
